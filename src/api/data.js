// These must be defined BEFORE they are used in the data generation

const getAllStates = () => {
  return HIERARCHY.states.map(s => s.name);
};

// Get districts for a state from hierarchy
const getDistricts = (state) => {
  if (!state) return [];
  const stateData = HIERARCHY.states.find(s => s.name === state);
  if (!stateData) return [];
  return stateData.districts.map(d => d.name);
};

// Get assembly constituencies for a state and district from hierarchy
const getAssemblyConstituencies = (state, district) => {
  if (!state || !district) return [];
  const stateData = HIERARCHY.states.find(s => s.name === state);
  if (!stateData) return [];
  const districtData = stateData.districts.find(d => d.name === district);
  if (!districtData) return [];
  
  const assemblies = [];
  districtData.parliamentConstituencies.forEach(pc => {
    pc.assemblyConstituencies.forEach(ac => {
      assemblies.push(ac.name);
    });
  });
  return assemblies;
};

// Get blocks for a state, district, and assembly constituency
const getBlocks = (state, district, assembly) => {
  if (!state || !district || !assembly) return [];
  const stateData = HIERARCHY.states.find(s => s.name === state);
  if (!stateData) return [];
  const districtData = stateData.districts.find(d => d.name === district);
  if (!districtData) return [];
  
  const blocks = [];
  districtData.parliamentConstituencies.forEach(pc => {
    pc.assemblyConstituencies.forEach(ac => {
      if (ac.name === assembly) {
        ac.blocks.forEach(block => {
          blocks.push(block.name);
        });
      }
    });
  });
  return blocks;
};

// Get panchayats for a state, district, assembly, and block
const getPanchayats = (state, district, assembly, block) => {
  if (!state || !district || !assembly || !block) return [];
  const stateData = HIERARCHY.states.find(s => s.name === state);
  if (!stateData) return [];
  const districtData = stateData.districts.find(d => d.name === district);
  if (!districtData) return [];
  
  const panchayats = [];
  districtData.parliamentConstituencies.forEach(pc => {
    pc.assemblyConstituencies.forEach(ac => {
      if (ac.name === assembly) {
        ac.blocks.forEach(b => {
          if (b.name === block) {
            b.panchayats.forEach(panchayat => {
              panchayats.push(panchayat.name);
            });
          }
        });
      }
    });
  });
  return panchayats;
};

